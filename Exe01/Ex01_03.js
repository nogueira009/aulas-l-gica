const prompt = require('prompt-sync')()

//ler um numero
let numero = Number(prompt("Digite um numero"));

//mostre seu dobro
console.log(`O valor do dobro é: ${numero * 2}`)

//mostrar seu triplo
console.log(`O valor do triplo é: ${numero * 3}`)

//mostrar seu valor ao cubo
console.log(`O valor do numero elevado au cubo é: ${numero ** 3 }`)